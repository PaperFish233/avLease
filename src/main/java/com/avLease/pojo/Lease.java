package com.avLease.pojo;

public class Lease {
	
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTap() {
		return tap;
	}
	public void setTap(String tap) {
		this.tap = tap;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public int getState() {
		return state;
	}
	public void setState(int state) {
		this.state = state;
	}
	private int no;
	private String name;
	private String tap;
	private String time;
	private int state;

}
